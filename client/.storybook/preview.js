// Theme decorator
const withTheme = (Story, context) => {
  const { theme } = context.globals;
  
  // Apply theme to document
  React.useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme);
  }, [theme]);

  return (
    <div data-theme={theme} className="theme-transition">
      <Story />
    </div>
  );
};

